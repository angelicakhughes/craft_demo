#print i num but break at 3
def whileloop3break():
    # set i to a starting number
    i =1
    #do something multiple times over
    while i <6:
        print (i)
        #but if its 3 break
        if i == 3:
            break
        #increase i by one each time you do it over
        i += 1
        
        
